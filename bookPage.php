            <h2>Call of Chtulhu</h2>
            <p>
                Call of Chtulhu is a collection of short horror stories are written by Howard Lovecraft. It was first published in February 1928.
                This stories have different plots and characters, but they relate to the one mysterious world, which is full of horrible monsters and ancient civilizations.
                The first seed of the story's first chapter The Horror in Clay came from one of Lovecraft's own dreams he had in 1919, which he described briefly in two different
                letters sent to his friend Rheinhart Kleiner on May 21 and December 14, 1920. In the dream, Lovecraft is visiting an antiquity museum in Providence, attempting to convince
                the aged curator there to buy an odd bas-relief Lovecraft himself had sculpted, who initially scoffs at him for trying to sell something recently made to a museum of antique objects.
                Lovecraft then remembers himself answering the curator with the response
                "Why do you say that this thing is new? The dreams of men are older than brooding Egypt or the contemplative Sphinx, or garden-girdled Babylon, and this was fashioned in my dreams."
                This can be compared to what the character of Henry Anthony Wilcox tells the main character's uncle while showing him his sculpted bas-relief for help in reading hieroglyphs on it
                which came through Wilcox's own fantastical dreams:
                "It is new, indeed, for I made it last night in a dream of strange cities; and dreams are older than brooding Tyre or the contemplative Sphinx, or garden-girdled Babylon."
                Lovecraft then used this for a brief synopsis of a new story outlined in his own Commonplace Book at first in August 1925, which developed organically out of the idea of what the bas-relief
                in the dream actually might have depicted. In a footnote for his writing down of his own dream, Lovecraft then finished with the suggestion "Add good development & describe nature of bas-relief"
                to himself for future reference.
